import UIKit

extension UIColor {
    
    static var customGray: UIColor {
        return UIColor(red: 155.0 / 255.0,
                       green: 155.0 / 255.0,
                       blue: 155.0 / 255.0,
                       alpha: 1)
    }
    
    static var customDarkGray: UIColor {
        return UIColor(red: 74.0 / 255.0,
                       green: 74.0 / 255.0,
                       blue: 74.0 / 255.0,
                       alpha: 1)
    }
    
}
